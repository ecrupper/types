package constants

// Number base values and bit sizes.
const (
	// BaseValue defines the base for the integer to be parsed.
	BaseValue = 10

	// RegInt defines the bit size 0, which defaults to int.
	RegInt = 0

	// LongInt defines the bit size 64 for an integer.
	LongInt = 64

	// RegFloat defines the bit size of 32 for a float.
	RegFloat = 32

	// LongFloat defines the bit size of 64 for a float.
	LongFloat = 64
)
